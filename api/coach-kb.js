function tokenize(text) {
  return String(text || '')
    .toLowerCase()
    .replace(/[^\p{L}\p{N}\s]/gu, ' ')
    .split(/\s+/)
    .filter(Boolean);
}

function retrieveCoachContext(query, topK) {
  const q = String(query || '');
  const qLower = q.toLowerCase();
  const qTokens = tokenize(q);
  const limit = Math.max(1, Math.min(10, Number(topK || 5))); // 增加返回的知識片段數量
  const scored = KB_ROWS.map((row) => {
    let score = 0;
    // 標籤完全匹配或包含關鍵字，權重更高
    for (const tag of row.tags) {
      const t = tag.toLowerCase();
      if (qLower.includes(t)) score += 5; // 提高完全匹配的權重
      for (const tok of qTokens) {
        if (t.includes(tok) || tok.includes(t)) score += 2; // 提高部分匹配的權重
      }
    }
    // 文本內容匹配，權重較低但仍重要
    for (const tok of qTokens) {
      if (tokenize(row.text).includes(tok)) score += 1; // 文本內容匹配
    }
    return { ...row, score };
  })
    .filter((r) => r.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit);

  return scored.map((r) => ({
    id: r.id,
    text: r.text,
    tags: r.tags,
    score: r.score,
  }));
}

